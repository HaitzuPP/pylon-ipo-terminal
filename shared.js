var THEME_LABEL = {ai:"AI", health:"Healthcare", robot:"Robotics"};

/* ---------- helpers ---------- */
function esc(s){return (s==null?"":(""+s)).replace(/&(?!amp;|lt;|gt;)/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;");}
function isListed(r){return r.status==="listed";}
function themeTags(theme){
  if(!theme) return '<span class="dash">—</span>';
  return theme.map(function(t){return '<span class="tag '+t+'">'+THEME_LABEL[t]+'</span>';}).join(" ");
}
function dealCell(r){return r.deal==="Undisclosed"?'<span class="dash">n/d</span>':esc(r.deal);}
function byDateDesc(a,b){return a.date<b.date?1:a.date>b.date?-1:0;}
function byDateAsc(a,b){return a.date<b.date?-1:a.date>b.date?1:0;}
function pctChange(r){if(r.ipoNum==null||r.curNum==null)return null;return (r.curNum-r.ipoNum)/r.ipoNum*100;}
function fmtChange(p){
  if(p==null)return '<span class="dash">—</span>';
  var s=(p>=0?"+":"")+p.toFixed(1)+"%";
  return '<span class="chg '+(p>=0?"up":"down")+'">'+s+'</span>';
}

function statHtml(l,v,s){return '<div class="ps-stat"><div class="ps-label">'+l+'</div><div class="ps-val">'+v+'</div><div class="ps-sub">'+s+'</div></div>';}

var STATUS_ORDER=[["reported","Reported"],["filed","Filed"],["approved","Hearing passed"],["confirmed","Date confirmed"],["listed","Listed"]];
